const String kGeometryPy =
    '"""\n'
    'Workshop Helpers - geometry.py\n'
    '\n'
    'Common geometry calculations used in fabrication and machining.\n'
    '\n'
    'Usage example:\n'
    '    from workshop_helpers.geometry import circle_area, bend_allowance\n'
    '    area = circle_area(diameter=50.0)\n'
    '    ba   = bend_allowance(thickness=2.0, angle_deg=90, radius=3.0)\n'
    '"""\n'
    'import math\n'
    '\n'
    '\n'
    'def circle_area(diameter):\n'
    '    """Return the area of a circle given its diameter."""\n'
    '    return math.pi * (diameter / 2.0) ** 2\n'
    '\n'
    '\n'
    'def circle_circumference(diameter):\n'
    '    """Return the circumference of a circle given its diameter."""\n'
    '    return math.pi * diameter\n'
    '\n'
    '\n'
    'def circle_diameter_from_area(area):\n'
    '    """Return the diameter of a circle given its area."""\n'
    '    return 2.0 * math.sqrt(area / math.pi)\n'
    '\n'
    '\n'
    'def rectangle_area(width, height):\n'
    '    """Return the area of a rectangle."""\n'
    '    return width * height\n'
    '\n'
    '\n'
    'def rectangle_perimeter(width, height):\n'
    '    """Return the perimeter of a rectangle."""\n'
    '    return 2.0 * (width + height)\n'
    '\n'
    '\n'
    'def triangle_area_base_height(base, height):\n'
    '    """Return the area of a triangle given base and perpendicular height."""\n'
    '    return 0.5 * base * height\n'
    '\n'
    '\n'
    'def hypotenuse(a, b):\n'
    '    """Return the hypotenuse of a right triangle with legs a and b."""\n'
    '    return math.sqrt(a ** 2 + b ** 2)\n'
    '\n'
    '\n'
    'def bend_allowance(thickness, angle_deg, radius, k_factor=0.33):\n'
    '    """\n'
    '    Calculate the flat-blank length consumed by a bend (bend allowance).\n'
    '\n'
    '    Parameters\n'
    '    ----------\n'
    '    thickness  : material thickness\n'
    '    angle_deg  : interior bend angle in degrees\n'
    '    radius     : inside bend radius\n'
    '    k_factor   : neutral-axis ratio (default 0.33, range 0.25-0.50)\n'
    '    """\n'
    '    angle_rad = math.radians(angle_deg)\n'
    '    return angle_rad * (radius + k_factor * thickness)\n'
    '\n'
    '\n'
    'def bend_deduction(thickness, angle_deg, radius, k_factor=0.33):\n'
    '    """Calculate bend deduction."""\n'
    '    ba = bend_allowance(thickness, angle_deg, radius, k_factor)\n'
    '    outside_setback = math.tan(math.radians(angle_deg / 2.0)) * (radius + thickness)\n'
    '    return 2.0 * outside_setback - ba\n'
    '\n'
    '\n'
    'def flat_blank_length(flange_a, flange_b, thickness, angle_deg, radius, k_factor=0.33):\n'
    '    """\n'
    '    Return the total flat-blank length for a single-bend part.\n'
    '\n'
    '    Parameters\n'
    '    ----------\n'
    '    flange_a   : length of the first flange (outside dimension)\n'
    '    flange_b   : length of the second flange (outside dimension)\n'
    '    thickness  : material thickness\n'
    '    angle_deg  : interior bend angle in degrees\n'
    '    radius     : inside bend radius\n'
    '    k_factor   : neutral-axis ratio (default 0.33)\n'
    '    """\n'
    '    bd = bend_deduction(thickness, angle_deg, radius, k_factor)\n'
    '    return flange_a + flange_b - bd\n'
    '\n'
    '\n'
